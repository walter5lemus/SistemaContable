    public void rellenarTabla() {

        for (int i = 0; i < seleccionados.size(); i++) {
            Object[] fila = new Object[3];   
           
            fila[0] = seleccionados.get(i).getCodigo(); //Lo que hay entre comillas son los campos de la base de datos
            fila[1] = seleccionados.get(i).getNombre();
            fila[2] = seleccionados.get(i).getHoras();
       
           
            modelo.addRow(fila); // Añade una fila al final del modelo de la tabla
        }

        jTable1.updateUI();//Actualiza la tabla

    }
    
    
    public void llenarEmpleados(){

        conectar();
     try { 
            String sentencia = "SELECT idempleado, nombreempleado, salario from empleado";
            Statement stat = conect.createStatement(HIDE_ON_CLOSE, NORMAL);
            ResultSet resultado = stat.executeQuery(sentencia);
            
            while(resultado.next()) {
                Empleado empleado = new Empleado();
                empleado.setCodigo(resultado.getInt("idempleado"));
                empleado.setNombre(resultado.getString("nombreempleado"));
                empleado.setSalario(resultado.getDouble("salario"));
                empleados.add(empleado);
            }
            
        }
        catch(SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al recuperar los datos de la base de datos");
            ex.printStackTrace();
        }
    
    }
    
    
