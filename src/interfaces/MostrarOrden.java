    public void mostrarOrden(){
     conectar();
        try {
            CallableStatement cstmt = conect.prepareCall("{call sic.sp_orden(?)}");
            cstmt.registerOutParameter("orden", Types.DOUBLE);
            cstmt.execute();
            Double outputValue = cstmt.getDouble("orden");
            ordenF.setText(String.valueOf(outputValue));
            orden=outputValue;

        } catch (Exception e) {
            e.printStackTrace();

        }
    }

